def big_string(string)
	chars = []
	string.split("").each do |thing|
		chars.push BigLetter.new thing.downcase
	end
	lines = []
	5.times do |line_num|
		line = ""
		chars.each do |letter|
			line += letter.get_line line_num
		end
		lines.push line
	end
	lines.join("\n")
end

